    private void divisores() {

        int nanterior = 1;
        int n;
        int quantdivant = 0;
        int quantdivatu = 0;
        String text = "";
        String colunas[] = {"Numero"};
        DefaultTableModel modelo = new DefaultTableModel(colunas, 0);
        for (int i = 1; i <= 10000;) {
            n = i;

            for (int z = 1; z <= i; z++) {

                if (i % z == 0) {
                    quantdivatu++;

                    // System.out.println(" divisor "+Integer.toString(z));
                }

            }

            if (quantdivant == quantdivatu) {
            modelo.addRow(new Object[] {nanterior});    
                System.out.println(" Numero " + Integer.toString(nanterior));
            }

            //System.out.println(" Quantdiant " + Integer.toString(quantdivant));
            nanterior = i;
            quantdivant = quantdivatu;
            quantdivatu = 0;
            i++;
            // System.out.println(" Quantiatu " + Integer.toString(quantdivatu));
        }
 Tabela.setModel(modelo);
    }

